// L'aggiornatore di stato: entra in azione SOLO se il tasto ha cambiato la modalità
void updateDisplayState()
{
  if (currentMode != lastDrawnMode)
  {
    u8g2.clearBuffer(); // Pulisce lo schermo in un colpo solo

    switch (currentMode)
    {
    case 1:
      u8g2.drawBitmap(0, 0, 72 / 8, 40, img_faccia);
      break;
    case 2:
      u8g2.drawBitmap(0, 0, 72 / 8, 40, img_spirale);
      break;
    case 3:
      u8g2.drawBitmap(0, 0, 72 / 8, 40, img_rgb);
      break;
    case 4:
      u8g2.drawBitmap(0, 0, 72 / 8, 40, img_saturn);
      break;
    case 5:
      drawText("RAINBOW", "SPIRAL");
      break;
    case 6:
      drawText("CENTER", "EXPLODE");
      break;
    case 7:
      drawText("BICYCLE", "SPOKES");
      break;
    case 8:
      drawText("GRAVITY", "FALL");
      break;
    case 9:
      drawText("EXPAND", "PULSE");
      break;
    case 10:
      drawText("VARIABLE", "PULSE");
      break;
    }

    u8g2.sendBuffer(); // Invia l'immagine allo schermo. Finito. Niente più traffico.
    lastDrawnMode = currentMode;
  }
}

// ==========================================
// SETUP
// ==========================================
void setup()
{
  Serial.begin(115200);
  delay(2000);

  pinMode(BUTTON_PIN, INPUT_PULLUP);

  Wire.begin(5, 6);
  u8g2.begin();
  u8g2.setBusClock(400000); // Fast I2C

  // Testo Iniziale
  u8g2.clearBuffer();
  u8g2.setFont(u8g2_font_ncenB10_tr);
  u8g2.drawStr((72 - u8g2.getStrWidth("TEO ZEN")) / 2, 25, "TEO ZEN");
  u8g2.sendBuffer();

  FastLED.addLeds<LED_TYPE, LED_PIN, COLOR_ORDER>(leds, NUM_LEDS).setCorrection(TypicalLEDStrip);
  FastLED.setBrightness(BRIGHTNESS);
  FastLED.setMaxPowerInVoltsAndMilliamps(5, 800);

  FastLED.clear();
  SafeSetLED(R1_START, CRGB::Green);
  FastLED.show();
  delay(800);
  FastLED.clear();
  FastLED.show();
}

